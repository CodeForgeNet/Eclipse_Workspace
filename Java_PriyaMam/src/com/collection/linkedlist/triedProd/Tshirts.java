package com.collection.linkedlist.triedProd;

public class Tshirts {
	
	private String type;
	private String id;
	private double price;
	
	public Tshirts(String type,String id , double price) {
		this.type = type;
		this.id = id;
		this.price = price;
	}
	
	public String getType() {
		return this.type;
	}
	
	public String getId() {
		return this.id;
	}
	
	public double getPrice() {
		return this.price;
	}

}
